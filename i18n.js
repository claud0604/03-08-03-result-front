function getCurrentLang() {
    var stored = localStorage.getItem('apl-lang');
    if (stored && translations[stored]) return stored;
    var nav = (navigator.language || '').slice(0, 2).toLowerCase();
    if (translations[nav]) return nav;
    return 'en';
}

function setLang(lang) {
    localStorage.setItem('apl-lang', lang);
    applyTranslations();
}

function t(key) {
    var lang = getCurrentLang();
    return (translations[lang] && translations[lang][key]) || translations.en[key] || key;
}

function applyTranslations() {
    var lang = getCurrentLang();
    var dict = translations[lang] || translations.en;
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var key = el.getAttribute('data-i18n');
        if (key in dict) el.textContent = dict[key];
        else if (key in translations.en) el.textContent = translations.en[key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-placeholder');
        if (dict[key]) el.placeholder = dict[key];
        else if (translations.en[key]) el.placeholder = translations.en[key];
    });
    // Update lang selector
    var sel = document.getElementById('langSelect');
    if (sel) sel.value = lang;
}
